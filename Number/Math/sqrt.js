// Math.sqrt() - повертає корінь квадратний.

console.log(Math.sqrt(25));// 5
console.log(Math.sqrt(16, 25));// 4
console.log(Math.sqrt('100'));// 10
console.log(Math.sqrt(0));// 0
console.log(Math.sqrt(-0));// -0
console.log(Math.sqrt(-4 + 4));// 4
console.log(Math.sqrt(8));// 2.8284271247461903
console.log(Math.sqrt(null));// 0
console.log(Math.sqrt(true));// 1
console.log(Math.sqrt(false));// 0

console.log(Math.sqrt(-16));// NaN бо '-'.
console.log(Math.sqrt('-169'));// NaN бо '-'.
