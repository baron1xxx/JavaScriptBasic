// toString() - повертає 'string'.
// Переопріділяє унаслідований від Object


console.log(5252582..toString());    // '5252582'
console.log(typeof 5252582..toString());    // string
console.log(5.252582.toString());    // '5.252582'
console.log(5.0000.toString());    // '5'
console.log(0.00001.toString());    // '0.00001'
console.log(0.0.toString());    // '0'
console.log(-0..toString());    // '-0'
console.log(+0..toString());    // '0'
console.log(+5..toString());    // '0'

